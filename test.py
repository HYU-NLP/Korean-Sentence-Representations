import torch

x = torch.randn(2, 3)

print(x)
# tensor([[-0.3451, -0.4706, -0.4531],
#         [-0.2416, -1.0426, -2.3414]])
print(x.shape)
# torch.Size([2, 3])

x1 = torch.cat((x, x, x), dim=0)
print(x1)
# tensor([[-0.3451, -0.4706, -0.4531],
#         [-0.2416, -1.0426, -2.3414],
#         [-0.3451, -0.4706, -0.4531],
#         [-0.2416, -1.0426, -2.3414],
#         [-0.3451, -0.4706, -0.4531],
#         [-0.2416, -1.0426, -2.3414]])
print(x1.shape)
# torch.Size([6, 3])

x2 = torch.cat((x, x, x), dim=1)
print(x2)
# tensor([[-0.3451, -0.4706, -0.4531, -0.3451, -0.4706, -0.4531, -0.3451, -0.4706, -0.4531],
#         [-0.2416, -1.0426, -2.3414, -0.2416, -1.0426, -2.3414, -0.2416, -1.0426, -2.3414]])
print(x2.shape)
# torch.Size([2, 9])

